package com.Rylen27.luckypixelmon.init;

import net.minecraftforge.oredict.OreDictionary;

public class OreDictionaryInit 
{
	public static void registerOres()
	{
		OreDictionary.registerOre("ingotAluminum", ModItems.ALUMINUM_INGOT);
		OreDictionary.registerOre("blockAluminum", ModBlocks.ALUMINUM_BLOCK);
		OreDictionary.registerOre("oreAluminum", ModBlocks.ALUMINUM_ORE);
		OreDictionary.registerOre("ingotHaileys", ModItems.HAILEYS_INGOT);
		OreDictionary.registerOre("blockHaileys", ModBlocks.HAILEYS_BLOCK);
		OreDictionary.registerOre("oreHaileys", ModBlocks.HAILEYS_ORE);
		OreDictionary.registerOre("ingotAbigails", ModItems.ABIGAILS_INGOT);
		OreDictionary.registerOre("blockAbigails", ModBlocks.ABIGAILS_BLOCK);
		OreDictionary.registerOre("oreAbigails", ModBlocks.ABIGAILS_ORE);
		OreDictionary.registerOre("ingotEmilys", ModItems.EMILYS_INGOT);
		OreDictionary.registerOre("blockEmilys", ModBlocks.EMILYS_BLOCK);
		OreDictionary.registerOre("oreEmilys", ModBlocks.EMILYS_ORE);
		OreDictionary.registerOre("oreDeathsteel", ModBlocks.DEATHSTEEL_ORE);
		OreDictionary.registerOre("ingotDeathsteel", ModItems.DEATHSTEEL_INGOT);
	}
}
